// Function to generate the URL for the NFT image using the imageNumber
function generateS3ImageUrl(imageNumber) {
    return `https://web3toolsimage.s3.eu-north-1.amazonaws.com/nftImages/img${imageNumber}.png`;
}

// Component to handle minting an NFT
function MintNFT({ imageNumber }) {
    // State to track whether the minting process is in progress
    const [isMinting, setIsMinting] = useState(false);

    // Function to handle the minting process
    const handleMint = async () => {
        if (!window.ethereum) {
            alert('Metamask is not installed. Please install Metamask.');
            return;
        }

        const web3 = new Web3(window.ethereum);

        try {
            setIsMinting(true);

            // Request user accounts from Metamask
            const accounts = await web3.eth.requestAccounts();
            const contractAddress = '0x6f71ef40b1647e96fabE462E269818280120e4cB'; // Replace with your actual contract address
            const contract = new web3.eth.Contract(ABI, contractAddress);

            // Call the mintNFT function on the contract and send Ether
            await contract.methods.mintNFT().send({ from: accounts[0], value: web3.utils.toWei('0.01', 'ether') });
            alert('NFT Minted successfully!');
        } catch (error) {
            console.error(error);
            alert('An error occurred while minting NFT.');
        } finally {
            setIsMinting(false);
        }
    };

    // Render the NFT minting component
    return (
        <div className='App-main'>
            <img src={generateS3ImageUrl(imageNumber)} alt='NFT' width={100} />
            <button onClick={handleMint} className='Btn' disabled={isMinting}>Buy NFT</button>
        </div>
    );
}

