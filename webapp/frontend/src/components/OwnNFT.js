// Function to generate the URL for the NFT image using the imageNumber
function generateS3ImageUrl(imageNumber) {
    return `https://web3toolsimage.s3.eu-north-1.amazonaws.com/nftImages/img${imageNumber}.png`;
}

// Component to handle displaying an owned NFT and actions like selling and transferring
function OwnNFT({ imageNumber }) {
    const [showModal, setShowModal] = useState(false);
    const [tokenId, setTokenId] = useState(0); // Add a state to store the token ID

    // Function to open the modal for selling or transferring the NFT
    const openModal = (id) => {
        setTokenId(id); // Set the token ID before opening the modal
        setShowModal(true);
    };

    // Function to close the modal
    const closeModal = () => {
        setShowModal(false);
    };
    const transferNFT = async () => {
        if (!window.ethereum) {
            alert('Metamask is not installed. Please install Metamask.');
            return;
        }

        const web3 = new Web3(window.ethereum); // Initialize Web3 with the current provider
       
        

        try {

            // Request user accounts from Metamask
            const accounts = await web3.eth.requestAccounts();
            const contractAddress = '0x6f71ef40b1647e96fabE462E269818280120e4cB'; // Replace with your actual contract address
            const contract = new web3.eth.Contract(ABI, contractAddress);
            // Call the contract's transferNFT function
            await contract.methods.transferNFT(accounts[0], tokenId).send({ from: accounts[0], value: web3.utils.toWei('0.01', 'ether')  });

            // You can perform additional actions after the transfer, such as updating UI, etc.
            alert('NFT transferred successfully!');

            closeModal();
        } catch (error) {
            console.error('Error transferring NFT:', error);
            alert('Error transferring NFT:', error);
        }
    };
    // Render the owned NFT component
    return (
        <div className="App-main">
            <img src={generateS3ImageUrl(imageNumber)} alt="NFT" width={100} />
            <div className="button-container">
                {/* <button className="Btn" onClick={openModal}>
                    Sell NFT
                </button> */}
                <button className="Btn" onClick={()=>openModal(imageNumber)}>
                    Transfer NFT
                </button>
            </div>
            {/* Render the TransferModal component with modal state and close function */}
            <TransferModal  showModal={showModal} closeModal={closeModal} transferNFT={transferNFT} />
        </div>
    );
}
